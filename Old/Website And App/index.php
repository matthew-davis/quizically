									<h3>The Game</h3>
									<br>
									<p>From the days of pen and paper, technology has moved on. The need to get other teams to 'dubiously' mark other team’s answers is past. Some quiz teams use technology to 'cheat'. In essence, what the honest quiz goer needs is an even playing field. They want more interactivity. They want a bit of useful analysis - what topics they're good at, how quick they are, where they rank, have they improved, where have they improved, how intelligent they are and where they and their pub ranks locally and nationally.</p>
									<p><strong><span style="color: #1ba0e2;">quizzically </span></strong> is building the next generation of quiz platforms to deliver on all of these player needs and increas the fun to be had in a trip down to the local for some banter and competition.</p>
									<span class="fa featured fa-beer" style="color: #ffd700"></span>
								</article>
							</div>
							<div class="6u">
								<article class="box box-style2">
									<h3>The Product</h3>
									<br>
									<p>No need to spend hours designing your own quizzes or paying someone to do it for you. Our quiz platform creates the pub quiz for you. You enter the quiz specifics you want, for example number of questions, difficulty... and the tool does the rest. It records prior question used at that location to limit question recurrence. And the platform can be calibrated to account for difficulty of questions on past user performance - maintaining a challenge, without making it too difficult or easy.</p>
									<p><strong><span style="color: #1ba0e2;">quizzically </span></strong>will also allow direct marketing during the quiz to every quizzee. This will allow you to push out specials and promotions to all players throughout the quiz. Further marketing can be pushed out to past quiz participants letting them know about upcoming events and promotions increasing the marketing reach of the pub.</p>
